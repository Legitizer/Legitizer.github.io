function rollBuild(){
    let sItem = Math.round(Math.random()*(starters.length-1));
    let cItem = Math.round(Math.random()*(4-1));
    let newList = [];

    if (offenseCB.checked == false && defenseCB.checked == false){
        offenseCB.checked = true
        defenseCB.checked = true
    }

    if (mageR.checked == true){
        item1.src = magical_core[cItem]['url']
        if (offenseCB.checked == true){
            newList = newList.concat(magical_OFFENSE);
            newList = newList.concat(mages_OFFENSE);
        }
        if (defenseCB.checked == true){
            newList = newList.concat(DEFENSE);
            newList = newList.concat(mages_DEFENSE);
        }
    }else if (assassinR.checked == true){
        item1.src = physical_core[cItem]['url']
        if (offenseCB.checked == true){
            newList = newList.concat(physical_OFFENSE);
            newList = newList.concat(assassins_OFFENSE);
        }
        if (defenseCB.checked == true){
            newList = newList.concat(DEFENSE);
            newList = newList.concat(assassins_DEFENSE);
        }
    }
    else if (hunterR.checked == true){
        item1.src = physical_core[cItem]['url']
        if (offenseCB.checked == true){
            newList = newList.concat(physical_OFFENSE);
            newList = newList.concat(hunters_OFFENSE);
        }
        if (defenseCB.checked == true){
            newList = newList.concat(DEFENSE);
            newList = newList.concat(hunters_DEFENSE);
        }
    }
    else if (warriorR.checked == true){
        item1.src = physical_core[cItem]['url']
        if (offenseCB.checked == true){
            newList = newList.concat(physical_OFFENSE);
            newList = newList.concat(warriors_OFFENSE);
        }
        if (defenseCB.checked == true){
            newList = newList.concat(DEFENSE);
            newList = newList.concat(warriors_DEFENSE);
        }
    }
    else if (guardianR.checked == true){
        item1.src = magical_core[cItem]['url']
        if (offenseCB.checked == true){
            newList = newList.concat(magical_OFFENSE);
            newList = newList.concat(guardians_OFFENSE);
        }
        if (defenseCB.checked == true){
            newList = newList.concat(DEFENSE);
            newList = newList.concat(guardians_DEFENSE);
        }
    }

    itemStarter.src = starters[sItem]['url']

    let r2 = Math.round(Math.random()*(newList.length-1));
    item2.src = newList[r2]['url']
    newList.splice(r2, 1)

    let r3 = Math.round(Math.random()*(newList.length-1));
    item3.src = newList[r3]['url']
    newList.splice(r3, 1)

    let r4 = Math.round(Math.random()*(newList.length-1));
    item4.src = newList[r4]['url']
    newList.splice(r4, 1)

    let r5 = Math.round(Math.random()*(newList.length-1));
    item5.src = newList[r5]['url']
    newList.splice(r5, 1)

    let r6 = Math.round(Math.random()*(newList.length-1));
    item6.src = newList[r6]['url']
    newList.splice(r6, 1)
}